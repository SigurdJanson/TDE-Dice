test_that("the results fit", {
  sp <- 0:20
  exp <- list(
    1, 2, 3, 4,
    c(1, 4), c(2, 4), c(3, 4),
    c(1, 3, 4), c(2, 3, 4), c(3, 3, 4),
    c(1, 3, 3, 4), c(2, 3, 3, 4), c(3, 3, 3, 4),
    c(1, 3, 3, 3, 4), c(2, 3, 3, 3, 4), c(3, 3, 3, 3, 4),
    c(1, 3, 3, 3, 3, 4), c(2, 3, 3, 3, 3, 4), c(3, 3, 3, 3, 3, 4), c(4, 3, 3, 3, 3, 4), c(5, 3, 3, 3, 3, 4)
  )
  for (i in 1:21) {
    expect_equal(
      binSkillPoints(sp[i]),
      exp[[i]]
    )
  }
})
